package org.techit.week2.fourday;

public class BitOperatorEx {
    //비트 연산자 활용
    public static void main(String[] args) {
        int val1 = 1;
        System.out.println(val1 << 1);
        System.out.println(val1 << 2);
        System.out.println(val1 << 4);
        System.out.println(val1 >>1);

        int val2 = 32;
        System.out.println(val2>>1);
    }
}
